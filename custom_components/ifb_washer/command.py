"""IFB washer command packet helpers."""

from __future__ import annotations

from collections.abc import Iterable


PROGRAM_TEMPLATE = [
    0x63,
    0x13,
    0x03,
    0x00,
    0x01,
    0x04,
    0x00,
    0x03,
    0xE8,
    0x28,
    0x00,
    0x00,
    0x00,
    0x0A,
    0x01,
    0x00,
    0x00,
    0x00,
    0x00,
    0x9C,
    0x38,
]

FIXED_COMMANDS = {
    "turn_on": bytes([0x63, 0x0A, 0x01, 0x00, 0x01, 0x01, 0x00, 0x00, 0x00, 0x00, 0x70, 0xE0]),
    "start": bytes([0x63, 0x0A, 0x01, 0x00, 0x01, 0x01, 0x00, 0x00, 0x00, 0x00, 0x70, 0xE0]),
    "pause": bytes([0x63, 0x0A, 0x01, 0x00, 0x01, 0x03, 0x00, 0x00, 0x00, 0x00, 0x72, 0xE4]),
    "cancel": bytes([0x63, 0x0A, 0x01, 0x00, 0x01, 0x04, 0x00, 0x00, 0x00, 0x00, 0x73, 0xE6]),
    "turn_off": bytes([0x63, 0x0A, 0x01, 0x00, 0x01, 0x12, 0x00, 0x00, 0x00, 0x00, 0x81, 0x02]),
    "child_lock_on": bytes([0x63, 0x0A, 0x02, 0x01, 0x0B, 0x01, 0x00, 0x00, 0x00, 0x00, 0x7C, 0xF8]),
    "child_lock_off": bytes([0x63, 0x0A, 0x02, 0x01, 0x0B, 0x00, 0x00, 0x00, 0x00, 0x00, 0x7B, 0xF6]),
}


def _signed_byte(value: int) -> int:
    value &= 0xFF
    return value - 0x100 if value >= 0x80 else value


def _first_checksum(command: Iterable[int]) -> int:
    values = list(command)
    return sum(_signed_byte(value) for value in values[:-2])


def _second_checksum(first: int) -> int:
    if first < 249:
        return first * 2
    text = format(first & 0xFFFF, "02x")
    return int(text[-2:-1], 16)


def _finish(command: list[int]) -> bytes:
    first = _first_checksum(command)
    command[-2] = first & 0xFF
    command[-1] = _second_checksum(first) & 0xFF
    return bytes(command)


def build_program_command(program_code: int) -> bytes:
    """Build the observed My IFB program selection command."""
    command = PROGRAM_TEMPLATE.copy()
    command[5] = program_code & 0xFF
    return _finish(command)


def build_fixed_command(command: str) -> bytes:
    """Return an observed fixed My IFB washer command."""
    try:
        return FIXED_COMMANDS[command]
    except KeyError as err:
        raise ValueError(f"Unknown IFB washer command: {command}") from err
